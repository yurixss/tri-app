// ─── Helpers ─────────────────────────────────────────────────────────

export function getProductById(id: string): Product | undefined {
  return PRODUCTS.find(p => p.id === id);
}

export function getProductsByCategory(category: Product['category']): Product[] {
  return PRODUCTS.filter(p => p.category === category);
}

export function getProductsByProblem(tag: Product['problemTags'][number]): Product[] {
  return PRODUCTS.filter(p => p.problemTags.includes(tag));
}

export function getProductsByBudget(tier: Product['budgetTier']): Product[] {
  const tierOrder = { baixo: 0, medio: 1, alto: 2 };
  return PRODUCTS.filter(p => tierOrder[p.budgetTier] <= tierOrder[tier]);
}
